// Writer
// ---------------------------------------------------------------------------

export type GoalMeta = {
	title?: string;
	description?: string;
};

export type KanbanContextCard = {
	id: string;
	title: string;
	lane: string;
	description?: string | null;
	branch?: string | null;
	prUrl?: string | null;
	sessionCount?: number;
	activeSessionId?: string | null;
	activeSessionStatus?: string | null;
	activeSessionAgentType?: string | null;
	assigneeName?: string | null;
};

function asRecord(value: unknown): Record<string, unknown> | undefined {
	return value && typeof value === "object" && !Array.isArray(value)
		? (value as Record<string, unknown>)
		: undefined;
}

function optionalString(value: unknown): string | null | undefined {
	if (typeof value === "string") return value;
	return value === null ? null : undefined;
}

function optionalNumber(value: unknown): number | undefined {
	return typeof value === "number" && Number.isFinite(value)
		? value
		: undefined;
}

export function normalizeKanbanContextCards(
	cards: readonly unknown[],
): KanbanContextCard[] {
	const normalized: KanbanContextCard[] = [];
	for (const rawCard of cards) {
		const card = asRecord(rawCard);
		if (!card) continue;

		const id =
			optionalString(card.childWorkspaceId) ??
			optionalString(card.workspaceId) ??
			optionalString(card.id);
		if (!id) continue;

		const title = optionalString(card.title) ?? "Untitled workspace";
		const lane =
			optionalString(card.lane) ?? optionalString(card.status) ?? "backlog";

		normalized.push({
			id,
			title,
			lane,
			description: optionalString(card.description),
			branch: optionalString(card.branch) ?? optionalString(card.branchName),
			prUrl: optionalString(card.prUrl),
			sessionCount: optionalNumber(card.sessionCount),
			activeSessionId: optionalString(card.activeSessionId),
			activeSessionStatus: optionalString(card.activeSessionStatus),
			activeSessionAgentType: optionalString(card.activeSessionAgentType),
			assigneeName: optionalString(card.assigneeName),
		});
	}
	return normalized;
}

export async function writeKanbanContext(
	cwd: string,
	cards: unknown[],
	goalMeta?: GoalMeta,
): Promise<void> {
	const normalizedCards = normalizeKanbanContextCards(cards);
	const piDir = join(cwd, ".pi");
	const extensionsDir = join(piDir, "extensions");
	const contextDir = join(piDir, "context");

	try {
		await Promise.all([
			mkdir(extensionsDir, { recursive: true }),
			mkdir(contextDir, { recursive: true }),
		]);

		const writes: Promise<void>[] = [
			writeFile(
				join(extensionsDir, "helmor-kanban.ts"),
				HELMOR_KANBAN_EXTENSION,
				"utf8",
			),
			writeFile(
				join(contextDir, "kanban.json"),
				JSON.stringify(normalizedCards, null, 2),
				"utf8",
			),
		];

		// Always write goal-meta.json (even if empty) so the extension can
		// always read it without a try/catch skip path for the first message.
		writes.push(
			writeFile(
				join(contextDir, "goal-meta.json"),
				JSON.stringify(goalMeta ?? {}, null, 2),
				"utf8",
			),
		);

		await Promise.all(writes);

		logger.debug("Wrote Pi Kanban context files", {
			cwd,
			cardCount: normalizedCards.length,
			hasGoalMeta: !!(goalMeta?.title ?? goalMeta?.description),
		});
	} catch (err) {
		// Non-fatal — the agent can still run without context injection.
		logger.info("Failed to write Pi Kanban context files", {
			cwd,
			error: String(err),
		});
	}
}
